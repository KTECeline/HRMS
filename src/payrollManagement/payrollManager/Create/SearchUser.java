    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchText = btnSearch.getText().trim().toLowerCase();
        String searchCriteria = (String) jComboBox1.getSelectedItem(); 
        String filePath = "usertxt.txt";
        File file = new File(filePath);

        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            br.readLine();
            String[] columnsName = {"Employee ID", "Employee Name", "Department", "Employment Type", "Date of Joined", "Bank Name", "Bank Account"};
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setColumnIdentifiers(columnsName);
            model.setRowCount(0);
            
            String line;
            while ((line = br.readLine()) != null) {
                String[] dataRow = line.split(",");
                if (dataRow.length >= 7){
                    String empID = dataRow[0].toLowerCase();
                    String empName = dataRow[1].toLowerCase();
                    String department = dataRow[13].toLowerCase(); 
                    String employmenttype = dataRow[15].toLowerCase();
                    String dateOfJoined = dataRow[14].toLowerCase();
                    String bankName = dataRow[20].toLowerCase();
                    String bankAccount = dataRow[21].toLowerCase();
                 
                    boolean match = false;

                    switch (searchCriteria) {
                        case "Employee ID":
                            match = empID.contains(searchText);
                            break;
                        case "Employee Name":
                            match = empName.contains(searchText);
                            break;
                        case "Department":
                            match = department.contains(searchText);
                            break; 
                        case "Employment Type":
                            match = employmenttype.contains(searchText);
                            break;
                        case "Date of Joined":
                            match = dateOfJoined.contains(searchText);
                            break;
                        case "Bank Name":
                            match = bankName.contains(searchText);
                            break;
                        case "Bank Account":
                            match = bankAccount.contains(searchText);
                            break;
                    }

                    if (match) {
                        String[] selectedData = {dataRow[0], dataRow[1], dataRow[13], dataRow[15], dataRow[14], dataRow[20], dataRow[21]};
                        model.addRow(selectedData); 
                    }
                }
            }

        } catch (IOException ex) {
            Logger.getLogger(SearchUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {                                     
        int selectedRow = table.getSelectedRow(); 

        String empID = table.getValueAt(selectedRow, 0).toString();
        String empName = table.getValueAt(selectedRow, 1).toString();
        String department = table.getValueAt(selectedRow, 2).toString();
        String employmenttype = table.getValueAt(selectedRow, 3).toString();
        String dateOfJoined = table.getValueAt(selectedRow, 4).toString();
        String bankName = table.getValueAt(selectedRow, 5).toString();
        String bankAccount = table.getValueAt(selectedRow, 6).toString();
        Create createForm = new Create();

        createForm.setButtonValues(empID, empName, department, employmenttype, dateOfJoined, bankName, bankAccount);

        createForm.setVisible(true);
        this.dispose();
    }
    
