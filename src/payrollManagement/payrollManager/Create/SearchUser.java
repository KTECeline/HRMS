    private void loadTableData() {
        String filePath = "usertxt.txt";
        File file = new File(filePath);

        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            br.readLine();
            String[] columnsName = {"Employee ID", "Employee Name", "Department", "Employment Type", "Date of Joined", "Bank Name", "Bank Account"};
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setColumnIdentifiers(columnsName);
            
            String line;
            while ((line = br.readLine()) != null) {
                String[] dataRow = line.split(",");
                if (dataRow.length >= 7){
                    String[] selectedData = {dataRow[0], dataRow[1], dataRow[13], dataRow[15], dataRow[14], dataRow[20], dataRow[21]};
                    model.addRow(selectedData); 
                }
            }

        } catch (IOException ex) {
            Logger.getLogger(TextFileDataToJTable.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

